<p>
  Pioneer is an experimental initiative led by Mozilla to better understand how
  our users use and navigate the internet. Pioneer is available to Firefox users
  in the United States who are 19 or older.
</p>
<p>
  When you join Pioneer, we’ll ask you to provide optional demographic data.
  We’ll also collect basic technical and interaction data as long as you’re
  participating in Pioneer. Once you’ve enrolled, you’ll have the opportunity to
  join available studies—each study will have a specific research purpose and
  unique privacy notice for you to review before you join it.
</p>
<p>
  In this Privacy Notice, we detail what data the Pioneer program collects and
  discloses, and why. Read each study’s privacy notice for information about how
  data is collected and handled in that particular study. We also adhere to the
  Mozilla Privacy Policy for how we receive, handle, and share information.
</p>

<h3>Information We Collect</h3>

<h4>Demographic data</h4>

<p>
  We collect optional, self-reported demographic data from Pioneer participants,
  including their age, gender, race/ethnicity, education level, household
  income, and zip code.
</p>

<h4>Technical data</h4>

<p>
  We collect basic information about your device’s operating system. When
  Firefox sends data to us, your IP address is temporarily collected as part of
  our server logs.
</p>

<h4>Interaction data</h4>
<p>
  We collect data about your interactions with Firefox, like number and type of
  installed Firefox Add-ons and your active browsing session duration.
</p>

<h4>Location data</h4>

<p>
  We will use your IP address to approximate your country location, in addition
  to collecting your self-reported zip code (if you provide it).
</p>

<p>See a full list of the data we collect.</p>

<h3>How We Use Your Information</h3>

<p>
  We use the information we collect for for research and development, including:
</p>

<ul>
  <li>
    To determine which participants meet the criteria to be available to
    participate in particular research studies
  </li>
  <li>
    To ensure our data sets are representative of the many users of Firefox
  </li>
  <li>
    To improve our existing products and services • To create and develop new
    products
  </li>
</ul>

<h3>Who We May Disclose Information To</h3>

<h4>Google Cloud Platform (GCP)</h4>

<p>
  We use GCP as our cloud-storage service. Mozilla has contracted with GCP
  requiring them to handle the data in ways that are approved by us.
</p>

<h4>Third-party researchers</h4>

<p>
  As part of being part of the Pioneer program, we will offer you the ability to
  join studies. If necessary for the study, we may ask you to share all or some
  of the data collected under this Privacy Notice with the third party
  researcher(s) administering a study. Mozilla will contractually obligate the
  third party researchers to ensure that your data is handled in ways that are
  approved by us.
</p>

<h4>General public</h4>

<p>
  To advance our mission of being open, we may release data sets to the general
  public. When we do so, we will aggregate the data and remove identifying
  information, so the data won’t reveal the behaviors or characteristics of
  individual users.
</p>

