<h2>Leaving the Study</h2>
<p>
    You can leave the study at any time from the Mozilla Rally options page,
    which lists the studies that you are currently enrolled in. If you leave the
    study, the study’s browser extension will be automatically uninstalled,
    removing the study code and data from your browser. We will also
    automatically delete the data that your browser has submitted for the study,
    unless you allow us to retain that data. You may not be able to rejoin the
    study if you leave. If you leave Rally, you will also leave the studies that
    you are enrolled in, including this study.
</p>
<p>
    If you joined the study by installing the Mozilla Rally extension, you can
    access the options page by clicking on the Rally button <img
        style="padding: 0 4px;"
        width="18"
        src="img/rally-toolbar-icon.svg"
        alt="rally icon"
    /> in your browser toolbar. The button is usually near the top right of the browser
    window. If you have removed the Rally button from your toolbar, you can also
    access the Rally options page from the browser’s Add-ons settings.
</p>

<p>
    If you joined the study without installing the Mozilla Rally extension, you
    can access the options page by navigating to <a
        href="https://rally.mozilla.org">https://rally.mozilla.org/</a
    >.
</p>

