<h2>Study Description</h2>
<p>
    The goal of this research is to understand how users interact with and make
    choices about general-purpose web search engines. Understanding the search
    engine marketplace will inform competition policy, promoting a diverse
    digital ecosystem that benefits users.
</p>

<h2>Study Procedures</h2>
<p>
    During your participation in the study, we will ask you to answer survey
    questions, and your browser will share information about your interactions
    with general-purpose web search engines and major online services that
    compete with services offered by search engine companies. We will not
    collect search queries, URLs, or webpage content. We will take a number of
    steps to protect your data, which we describe below.
</p>
<p>
    You may be randomly selected for one of two types of changes to your
    browsing experience. In the first type of change, we may prompt you to
    consider your choice of web search engine in your browser settings. You will
    remain free to select any web search engine of your choice. In the second
    type of change, we may minimize or omit web search results that highlight a
    search engine’s own services or a direct answer from a third-party source.
    We will not alter any ordinary (“organic”) web search results or search
    engine advertising, and you will remain free to select any online services
    of your choice.
</p>

<h2>Data Collection</h2>
<p>
    The specific types of data that we will collect in this study are as
    follows.
</p>

<ul class="mzp-u-list-styled">
    <li>
        Your responses to survey questions about web search and your
        demographics.
    </li>
    <li>How you use general-purpose web search engines.</li>
    <li>Information about the search engine result pages that you navigate.</li>
    <li>
        How often you used web search engines prior to joining the study, 
        going back up to 30 days in your browser’s history.
    </li>
    <li>
        If we prompt you to consider your choice of web search engine,
        information about how you interact with that prompt.
    </li>
    <li>
        How you interact with other online services offered by a general-purpose 
        web search engine company.
    </li>
    <li>
        How you interact with online services that compete with services offered by 
        a general-purpose web search engine company.
    </li>

    If you would like further detail about the data collected in this study, the
    complete software implementation of the study is publicly available on
    <a href="https://github.com/mozilla-rally/search-engine-usage-study"
        >GitHub</a
    >.
</ul>

<h2>Data Protection</h2>
<p>
    We have taken a number of steps to protect the data associated with this
    study.
</p>

<ul class="mzp-u-list-styled">
    <li>
        Our research team is minimizing the data that we collect. We will not
        record search queries, URLs, or webpage content, and we will limit data
        collection to a specific set of websites.
    </li>
    <li>
        We will not collect information that can readily identify you, such as
        your name, email address, or telephone number.
    </li>
    <li>
        We will not record your Internet Protocol address when you submit data
        for the study.
    </li>
    <li>
        We will not attempt to re-identify study data on the basis of the
        information that we do collect.
    </li>
    <li>
        We are encrypting all study data that we collect both in transit and at
        rest.
    </li>
    <li>
        We have applied access controls to study data, which allow access only
        by our research team and a small number of Mozilla staff collaborating
        with our team.
    </li>
    <li>
        We store and analyze study data in a restricted computing environment.
        We maintain auditable logs for the data analysis environment, and we
        audit those logs to confirm that study data was used appropriately.
    </li>
    <li>
        If you choose to leave the study, you can instruct us to promptly delete
        your study data.
    </li>
    <li>
        We will end data collection for this study no later than December 31, 2022.
    </li>
    <li>
        We will discard participant-level study data no later than June 30, 2023.
    </li>
</ul>

